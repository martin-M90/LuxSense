static void SetActiveLED(uint8_t idx)
{
  /* Apaga los 3 LEDs de placa */
  HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);

  switch (idx % 3U)
  {
    case 0U:
      /* LD1 ON + patrón para el anillo */
      HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_SET);
      ring_fill(40, 0,   0);   // ejemplo: todo rojo ~50% (ajusta a tu gusto)
      break;

    case 1U:
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
      ring_fill(10, 5, 0);   // igual que tu for
      // ws2812_show(&ws2812);   // [No verificado] si tu librería lo requiere
      break;

    default: /* case 2U */
      /* LD3 ON + otro patrón para el anillo */
      HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
      ring_fill(10,   10, 10);   // ejemplo: todo azul ~50%
      break;
  }
}
/* Callback HAL al atender EXTI en stm32h7xx_it.c */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if (GPIO_Pin == BTN_Pin) {
    uint32_t now = HAL_GetTick();
    if ((now - g_last_irq_ms) < BTN_DEBOUNCE_MS) {
      return; // debounce
    }
    g_last_irq_ms = now;

    g_led_index = (uint8_t)((g_led_index + 1U) % 3U);
    SetActiveLED(g_led_index);
  }
}
